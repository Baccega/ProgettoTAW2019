const mongoose =  require("mongoose");

let userSchema = new mongoose.Schema ({
    name: {
        type: String,
        required: true,
        min: 6,
        max: 255
    },
    email: {
        type: String,
        required: true,
        min: 6,
        max: 255
    },
    password: {
        type: String,
        required: true,
        min: 6,
        max: 255
    },
    role: {
        type: String,
        required: true
    }
});

module.exports = mongoose.model ("User", userSchema);